	private static boolean match(String path, String patterns[]) {
		for (int i = 0; i < patterns.length; i++)
			if (path.indexOf(patterns[i]) >= 0)
				return true;

		return false;
	}

	private static boolean equals(String path, String patterns[]) {
		for (int i = 0; i < patterns.length; i++)
			if (path.equals(patterns[i]))
				return true;

		return false;
	}

	public static String checkPath(String path) {
		if (path != null && !equals(path, systemPatterns))
			if (!path.startsWith(".") && !match(path, notAllowedPatterns)) {
				int len = path.length();
				for (int i = 0; i < len; i++) {
					char c = path.charAt(i);
					if ((c & 0xff00) != 0)
						return null;
					if (!Character.isDigit(c) && !Character.isLetter(c)
							&& c != '/' && c != '.' && c != '-' && c != '_'
							&& c != '\\' && c != ':')
						return null;
				}

			} else {
				return null;
			}
		return path;
	}

