	public static String userHash(String userURL) {
		int h = hash(userURL);
		char ndb[] = intToHexa(h & 0xf, 1);
		char nn1[] = intToHexa(h >> 4 & 0x7f, 2);
		char nn2[] = intToHexa(h >> 11 & 0xff, 2);
		StringBuffer userPath = new StringBuffer("/");
		userPath.append(ndb);
		userPath.append("/");
		userPath.append(nn1);
		userPath.append("/");
		userPath.append(nn2);
		userPath.append("/");
		userPath.append(userURL);
		return userPath.toString();
	}

