	public static int hash(String s) {
		int ret = 0;
		int len = s.length();
		for (int i = 0; i < len; i++) {
			ret += vHash[s.charAt(i)][i & 0x1f];
			if (ret >= 0x80000)
				ret -= 0x80000;
		}

		return ret;
	}

	private static char[] intToHexa(int number, int size) {
		char ret[] = new char[size];
		for (int i = size - 1; i >= 0; i--) {
			ret[i] = HEX_REPRESENTATION[number & 0xf];
			number >>= 4;
		}

		return ret;
	}

